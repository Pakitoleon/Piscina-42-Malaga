#include<unistd.h>

void	ft_print_comb(char re)
{
	re[0] = '0';
	while (re[0] <= '7')
	{
		re[1] = re[0] + 1;
		while (re[1] <= '8')
		{
			re[2] = re[1] + 1;
			while (re[2] <= '9')
			{
				write(1, &re[0], 1);
				write(1, &re[1], 1);
				write(1, &re[2], 1);
				if (!(re[0] == '7' && re[1] == '8' && re[2] == '9'))
				write(1, ",", 1), write(1, " ", 1);
				re[2]++;
			}
			re[1]++;
		}
		re[0]++;
	}
}

int main(void)
{
	ft_print_comb(re);
	return (0);
}